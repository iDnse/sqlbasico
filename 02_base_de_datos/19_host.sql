/* Hospedaje SQL
Si desea que su sitio web pueda almacenar y recuperar datos de una base de datos,
su servidor web debe tener acceso a un sistema de base de datos que utilice el lenguaje SQL.

Si su servidor web está alojado por un proveedor de servicios de Internet 
(ISP), tendrá que buscar planes de alojamiento SQL.

Las bases de datos de alojamiento SQL más comunes son MS SQL Server, 
Oracle, MySQL y MS Access.*/

/* MySQL
